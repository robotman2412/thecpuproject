	public short getAddress() {
		int address;
		if (mode == 0) { // Find address in ISAROM.
			address = ((regIR & 0x7F) << 4) | stage;
		} else {
			address = (mode << 4) | stage | (1 << 11);
		}
		int ctrl;
		if (address >= isa.length) {
			ctrl = 0;
		} else {
			ctrl = isa[address]; // Get control signals.
		}
		short res = adrBus;
		if ((ctrl & 0x00008000) > 0) { // FCX control signal
			res += ((int) regX) & 0xff;
		}
		if ((ctrl & 0x08000000) > 0) { // ADC control signal
			res++;
		}
		return res;
	}
	

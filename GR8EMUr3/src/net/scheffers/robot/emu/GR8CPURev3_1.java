	public static void loadExtract(String resource) throws IOException {
		String dir = System.getProperties().getProperty("java.io.tempdir");
		File how = new File(dir, "gr8cpu_temp_lib");
		if (!how.exists()) {
			how.mkdir();
			how.deleteOnExit();
		}
		InputStream in = GR8CPURev3_1.class.getClassLoader().getResourceAsStream(resource);
		if (in == null) {
			throw new FileNotFoundException(resource);
		}
		File resulting = new File(how, resource);
		OutputStream out = new FileOutputStream(resulting);
		while (in.available() > 0) {
			byte[] garbage = new byte[in.available()];
			int why = in.read(garbage);
			out.write(garbage, 0, why);
		}
		out.close();
		in.close();
		System.load(resulting.getAbsolutePath());
	}
	
